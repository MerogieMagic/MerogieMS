function start() {
    status = -1;
    action(1, 0, 0);
}

function action(mode, type, selection) {
    if (mode < 1)  // disposing issue with stylishs found thanks to Vcoc
    {
        cm.dispose();
    } else {
        if (mode == 1) {
            status++;
        } else {
            status--;
        }
        if (status == 0) {
            var lines    = [];
            for (var i=0; i <= 149; i++) {
                lines.push(
                "#b#L" + i + "#" +
                "Page: " + (i + 1) +
                "#l"
                );
            }
            cm.sendSimple("Hello! I'm can Style your hair for a cost of 10k nx.\r\n" + lines.join("\r\n"));
        } else if (status == 1) {
            beauty = 1;
            page = selection;
            hairnew = hairSelection(page)
            console.log(hairnew)
            cm.sendStyle("I can totally change up your hairstyle and make it look so good. Why don't you change it up a bit? If you have #b#t5150003##k I'll change it for you. Choose the one to your liking~.", hairnew);
        } else if (status == 2) {
            cm.dispose();
            if (beauty == 1) {
                if (cm.getCashShop().getCash(1) >= 10_000) {
                    cm.setHair(hairnew[selection]);
                    cm.gainCash(-10000);
                    cm.sendOk("Enjoy your new and improved hairstyle!");
                } else {
                    cm.sendOk("Hmmm...it looks like you don't have enough NX...I'm afraid I can't give you a haircut without it. I'm sorry...");
                }
            }
        }
    }
}

function hairSelection(page) {
    hairnew = Array();
    const start   = page * 30;
    const end     = Math.min(start + 30, mhair_v.length);
    const hairMod = cm.getPlayer().getHair() % 10;
    for (let i = start; i < end; i++) {
        console.log(mhair_v[i])
        pushIfItemExists(hairnew, mhair_v[i] + hairMod);
    }
    return hairnew;
}